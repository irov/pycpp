#include "pycpp/factorable.hpp"
#include "pycpp/factory.hpp"

namespace pycpp
{
    //////////////////////////////////////////////////////////////////////////
    factorable::factorable()
        : m_factory( nullptr )
    {
    }
    //////////////////////////////////////////////////////////////////////////
    factorable::~factorable()
    {
    }
    //////////////////////////////////////////////////////////////////////////
    void factorable::set_factory( factory * _factory )
    {
        m_factory = _factory;
    }
    //////////////////////////////////////////////////////////////////////////
    void factorable::destroy()
    {
		m_factory->destroy_object( this );
    }
}
