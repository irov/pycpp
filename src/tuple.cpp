#include "pycpp/tuple.hpp"

namespace pycpp
{
    //////////////////////////////////////////////////////////////////////////
    void tuple::reserve( size_t _size )
    {
        m_elements.reserve( _size );
    }
    //////////////////////////////////////////////////////////////////////////
    void tuple::append( const pycpp::object_ptr & _object )
    {
        m_elements.emplace_back( _object );
    }
    //////////////////////////////////////////////////////////////////////////
    const pycpp::object_ptr & tuple::get( size_t _index ) const
    {
        return m_elements[_index];
    }
    //////////////////////////////////////////////////////////////////////////
    size_t tuple::size() const
    {
        return m_elements.size();
    }
    //////////////////////////////////////////////////////////////////////////
}